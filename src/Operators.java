public class Operators {
    public static void main (String[] args){

        // Arithmetic Operator (+.-,*,/,%,++,--)

        int a = 4;
        int b = 5;
        int sum = a+b;
        System.out.println(sum);

        // Assignment Operator (=,+=)

        int d = 5;
        d += 6;
        System.out.println(d);

        // Comparison Operator (==, <=, >=)

        System.out.println(6<=6);

        // Logical Operator (&&, ||, !)

        System.out.println(64<34 && 64>36);
        System.out.println(64<34 || 64>36);
        System.out.println(64!=64);

        // Bitwise Operators (&, |)
        System.out.println(2&3);


    }
}
